class SingerViewer : LinearLayout {
    var textView: TextView? = null
    var textView2: TextView? = null
    var imageView: ImageView? = null

    constructor(context: Context) : super(context) {
        init(context)
    }

    constructor(context: Context, attrs: AttributeSet?) : super(context, attrs) {
        init(context)
    }

    fun init(context: Context) {
        val inflater: LayoutInflater =
            context.getSystemService(Context.LAYOUT_INFLATER_SERVICE) as LayoutInflater
        inflater.inflate(R.layout.singer_shop_item, this, true)
        textView = findViewById<View>(R.id.textView) as TextView?
        textView2 = findViewById<View>(R.id.textView2) as TextView?
        imageView = findViewById<View>(R.id.imageView) as ImageView?
    }

    fun setItem(singerItem: SingerShopItem) {
        textView.setText(singerItem.getName())
        textView2.setText(singerItem.getCost().toString())
        imageView!!.setImageResource(singerItem.getImage())
    }
}